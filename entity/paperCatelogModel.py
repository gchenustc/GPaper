class PaperCatelog:

    id = -1
    sortedId = -1
    catelogName = ""
    catelogDesc = ""

    def __init__(self, sortedId=sortedId, catelogName=catelogName, catelogDesc=catelogDesc):
        self.sortedId = sortedId
        self.catelogName = catelogName
        self.catelogDesc = catelogDesc

    def id_construct(self, id=id):
        self.id = id
